    function advanceProjectStage(){
        
        var string = message.channel.name.replace('-project-text', '');

        console.log(string);
            
        for(p = 0; p < string.length; p++){
            if(string.charAt(p) == '-'){
                string = string.replace('-',' ');
            }
        }

        console.log(string);

        database.checkExists(message.guild.id, string, function(err, display){
            if(err){
                console.log("\\‚ùó Something went wrong displaying the projects.", err);
            }
            else{

                var state;

                switch(display[0].projectstate){
                    case 'created':
                        state = 'in-planning';
                        break;
                    case 'in-planning':
                        state = 'in-development';
                        break;
                    case 'in-development':
                        state = 'finished';
                        createPDF(display[0].projectname);
                        break;
                    case 'finished':
                        message.channel.send('\\‚ùó This project was already marked as finished it\'s project state cannot be advanced any further.');
                        return;
                }

                database.changeField(message.guild.id, string, 'projectstate', state, function(err, fieldResult){
                    if(err){
                        console.log(err);
                    }
                    else{
                        console.log(fieldResult);
                        message.channel.send(`\\‚úÖ **Project State Successfully progressed to ${state}.**`);
                    }
                });

            }
        });
    }

